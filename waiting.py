with sqlite3.connect("waiting.db") as conn:
    cursor = conn.cursor()
    cursor.execute(
        """
        pragma foreign_keys
        = on
        ;
    """
    )

    cursor.execute(
        """
        CREATE TABLE waiting_status (
            id integer PRIMARY KEY,
            name text NOT NULL
        );
    """
    )

    cursor.execute(
        """
        CREATE TABLE categories (
            id integer PRIMARY KEY,
            name text NOT NULL
        );
    """
    )

    cursor.execute(
        """
        CREATE TABLE types (
            id integer PRIMARY KEY,
            name text NOT NULL
        );
    """
    )

    cursor.execute(
        """
        CREATE TABLE packages (
            id integer PRIMARY KEY,
            name text NOT NULL UNIQUE,
            description text NOT NULL,
            url text NOT NULL,
            categories integer NOT NULL,
            waiting_status integer NOT NULL,
            added_date text NOT NULL,
            finished_date text,
            type integer NOT NULL DEFAULT 1,
            FOREIGN KEY (waiting_status)
            REFERENCES waiting_status (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT
            FOREIGN KEY (categories)
            REFERENCES categories (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT
            FOREIGN KEY (type)
            REFERENCES types (id)
            ON UPDATE RESTRICT
            ON DELETE RESTRICT
        );
    """
    )

    cursor.execute(
        f"""
        CREATE VIEW v_packages
        AS
        SELECT '[[' || url || '][' || name || ']]' AS package, description FROM packages WHERE waiting_status = {Waiting_Status.WAITING.value} ORDER BY added_date;
    """
    )

    cursor.execute(
        f"""
        CREATE VIEW v_need_packaging
        AS
        SELECT '[[' || url || '][' || name || ']]' AS package, description FROM packages WHERE waiting_status = {Waiting_Status.NOT_PACKAGED.value} ORDER BY added_date;
    """
    )

    cursor.execute(
        f"""
        INSERT INTO waiting_status  VALUES ({Waiting_Status.WAITING.value},'{Waiting_Status.WAITING.name}'), ({Waiting_Status.DONE.value},'{Waiting_Status.DONE.name}'), ({Waiting_Status.NOT_PACKAGED.value}, '{Waiting_Status.NOT_PACKAGED.name}'), ({Waiting_Status.REMOVED.value}, '{Waiting_Status.REMOVED.name}');
    """
    )

    cursor.execute(
        f"""
        INSERT INTO types VALUES ({Types.PACKAGE.value},'{Types.PACKAGE.name}'), ({Types.MODULE.value},'{Types.MODULE.name}');
    """
    )

    cursor.execute(
        f"""
        INSERT INTO categories VALUES
        ({Categories.MUSIC.value},'{Categories.MUSIC.name}'),
        ({Categories.NIX.value},'{Categories.NIX.name}'),
        ({Categories.SHELL.value},'{Categories.SHELL.name}'),
        ({Categories.DEV.value},'{Categories.DEV.name}'),
        ({Categories.SYSTEM.value},'{Categories.SYSTEM.name}'),
        ({Categories.GIT.value},'{Categories.GIT.name}'),
        ({Categories.WEB.value},'{Categories.WEB.name}'),
        ({Categories.NETWORK.value},'{Categories.NETWORK.name}'),
        ({Categories.DOCKER.value},'{Categories.DOCKER.name}'),
        ({Categories.AUDIO.value},'{Categories.AUDIO.name}'),
        ({Categories.SHARE.value},'{Categories.SHARE.name}'),
        ({Categories.DOC.value},'{Categories.DOC.name}'),
        ({Categories.IMAGE.value},'{Categories.IMAGE.name}'),
        ({Categories.ENCRYPT.value},'{Categories.ENCRYPT.name}'),
        ({Categories.FONT.value},'{Categories.FONT.name}'),
        ({Categories.GAME.value},'{Categories.GAME.name}'),
        ({Categories.VIDEO.value},'{Categories.VIDEO.name}'),
        ({Categories.DOWNLOAD.value},'{Categories.DOWNLOAD.name}'),
        ({Categories.PROXY.value},'{Categories.PROXY.name}');
    """
    )


