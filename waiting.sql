    ON UPDATE RESTRICT
    ON DELETE RESTRICT
    FOREIGN KEY (categories)
    REFERENCES categories (id)
    ON UPDATE RESTRICT
    ON DELETE RESTRICT
    FOREIGN KEY (type)
    REFERENCES types (id)
    ON UPDATE RESTRICT
    ON DELETE RESTRICT
);

