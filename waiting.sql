    id integer PRIMARY KEY,
    name text NOT NULL UNIQUE,
    description text NOT NULL,
    url text NOT NULL,
    categories integer NOT NULL,
    waiting_status integer NOT NULL,
    added_date text NOT NULL,
    finished_date text,
    type integer NOT NULL DEFAULT 1,
